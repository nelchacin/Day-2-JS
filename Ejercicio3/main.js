let askLenguage = prompt("What language do you want to translate 'Hello World'?")
let lenguage = ""
if (askLenguage == "spanish") {
    lenguage = alert ('Hola, mundo!.');
} else if (askLenguage == "french") {
    lenguage = alert ('Bonjour tout le monde.');
} else if (askLenguage == "italian") {
    lenguage = alert ('Ciao mondo!');
}
else {
    lenguage = alert ("Hello World!")

}